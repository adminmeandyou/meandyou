export function EmojiPicker({ onPick, onClose }: {
  onPick: (emoji: string) => void
  onClose?: () => void
}) {
  const [activeCat, setActiveCat] = useState(CATEGORIES[0].id)
  const current = CATEGORIES.find(c => c.id === activeCat) ?? CATEGORIES[0]

  return (
    <div style={{
      background: 'var(--bg-card2)',
      borderRadius: 14,
      border: '1px solid var(--border)',
      overflow: 'hidden',
      display: 'flex',
      flexDirection: 'column',
      maxHeight: 280,
    }}>
      {/* Tabs de categorias */}
      <div style={{
        display: 'flex',
        gap: 2,
        padding: '6px 6px 0',
        borderBottom: '1px solid rgba(255,255,255,0.04)',
        overflowX: 'auto',
      }}>
        {CATEGORIES.map(cat => (
          <button
            key={cat.id}
            onClick={() => setActiveCat(cat.id)}
            title={cat.label}
            style={{
              flexShrink: 0,
              width: 38,
              height: 36,
              border: 'none',
              background: activeCat === cat.id ? 'rgba(225,29,72,0.12)' : 'transparent',
              borderBottom: activeCat === cat.id ? '2px solid var(--accent)' : '2px solid transparent',
              borderRadius: '6px 6px 0 0',
              fontSize: 18,
              cursor: 'pointer',
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
              transition: 'background 0.15s',
            }}
          >
            {cat.icon}
          </button>
        ))}
      </div>

      {/* Grid de emojis */}
      <div style={{
        flex: 1,
        overflowY: 'auto',
        padding: 10,
        display: 'grid',
        gridTemplateColumns: 'repeat(auto-fill, minmax(34px, 1fr))',
        gap: 2,
      }}>
        {current.emojis.map((e, i) => (
          <button
            key={`${current.id}-${i}`}
            onClick={() => onPick(e)}
            style={{
              background: 'none',
              border: 'none',
              fontSize: 22,
              cursor: 'pointer',
              padding: 4,
              borderRadius: 6,
              lineHeight: 1,
              transition: 'background 0.12s',
            }}
            onMouseEnter={(ev) => (ev.currentTarget.style.background = 'rgba(255,255,255,0.06)')}
            onMouseLeave={(ev) => (ev.currentTarget.style.background = 'none')}
          >
            {e}
          </button>
        ))}
      </div>
    </div>
  )
}
